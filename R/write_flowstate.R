## convert non-numeric (factored; logical) [['data']] columns
## update [['parameters']]
## needs reassignment
data.parameters.update <- function(flowstate){
  ##
  data.cols.to.numeric <- flowstate[['data']][
    ,
    j = {
      res <- sapply(.SD, class)
      res[res != "numeric"]
    },
    .SDcols = !'sample.id'
  ]
  ##
  for(j in names(data.cols.to.numeric)){
    data.table::set(
      x = flowstate[['data']],
      j = j,
      value = as.numeric(flowstate[['data']][[j]])
    )
  }
  ##
  flowstate$parameters <- rbind(
    flowstate$parameters,
    data.table::data.table(
      par = paste0(
        '$P',
        (seq(length(data.cols.to.numeric)) + flowstate$parameters[, .N])
      ),
      B = "32",
      DISPLAY = "LIN",
      E = "0,0",
      N = names(data.cols.to.numeric),
      R = flowstate$data[
        ,
        j = sapply(.SD, function(j){as.character(max(ceiling(as.numeric(j))))}),
        .SDcols = names(data.cols.to.numeric)
      ],
      TYPE = sprintf("Derived_%s", tools::toTitleCase(data.cols.to.numeric))
    ),
    fill = TRUE
  )
  ##
  invisible(flowstate)
}

## convert non-character (factored) [['keywords']] columns
## update [['keywords']]
keywords.to.character <- function(flowstate){
  keywords.cols.to.character <- flowstate[['keywords']][
    ,
    j = {
      res <- sapply(.SD, class)
      res[res != "character"]
    },
    .SDcols = !'sample.id'
  ]
  ## convert to character in [['keywords']]
  for(j in names(keywords.cols.to.character)){
    if(keywords.cols.to.character[[j]] == "factor"){
      .value <- sprintf(
        "FACTOR::%s",
        paste0(flowstate$keywords[[j]], collapse = ";")
      )
      ##
      data.table::set(
        x = flowstate[['keywords']],
        j = j,
        value = .value
      )
      flowstate[['keywords']][, (j) := as.character(.SD[[j]])]
    }
  }
}

